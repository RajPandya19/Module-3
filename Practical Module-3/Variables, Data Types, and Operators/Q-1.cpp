// - Program with variables & constants of different types.
#include <iostream>
using namespace std;

int main() {
    // Variables of different types
    int age = 25;
    float price = 99.99;
    char grade = 'A';
    string name = "Raj";

    // Constant
    const double PI = 3.14159;

    cout << "Name: " << name << endl;
    cout << "Age: " << age << endl;
    cout << "Price: $" << price << endl;
    cout << "Grade: " << grade << endl;
    cout << "Value of PI: " << PI << endl;

    return 0;
}
